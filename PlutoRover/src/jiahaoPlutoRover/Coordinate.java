package jiahaoPlutoRover;

// class to store coordinate points
public class Coordinate {

	int first;
	int second;
	
	public Coordinate(int first, int second) {
		this.first = first;
		this.second = second;
	}
	
	public int getFst() {
		return first;
	}
	
	public int getSnd() {
		return second;
	}

}
